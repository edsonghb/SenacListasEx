let n1 = Number(prompt("Digite o primeiro número:"))
let n2 = Number(prompt("Digite o segundo número:"))
if (n1 == n2){
    alert("Os números são iguais")
}
else if (n1>n2){
     alert(`O número ${n1} é maior que o número${n2}`)
}
else if (n1<n2){
     alert(`O número ${n1} é menor que o número${n2}`)
}